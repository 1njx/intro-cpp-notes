#include <iostream>
using namespace std;


int main()
{
	// int - whole number (integer)
	int number1 = 5;
	cout << number1 << "\n";

	int total = number1 * 100;
	cout << total << "\n";

	// float - decimal number that goes up to 7 decimals
	float number2 = 3.14;

	// double - decimal number that goes up to 15 decimal places
	double number3 = 3.14;

	// char - single keyboard character (ascii table)
	char letter = 'B';
	
	// string - multiple characters
	string word2 = "pie";
	string word1 = "i love";
	string sentence = word1 + " " + word2;
	cout << sentence + "\n";

	// bool - true or false (boolean)
	bool check = true;
	cout << check;

	cout << "\nHello World!\n";
}//end main
